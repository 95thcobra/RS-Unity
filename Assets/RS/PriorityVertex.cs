namespace RS
{
    /// <summary>
    /// Represents a RS vertex.
    /// </summary>
    public class PriorityVertex
    {
        /// <summary>
        /// The depth/priority of the vertex.
        /// </summary>
        public int W;
        /// <summary>
        /// The x coordinate of the vertex.
        /// </summary>
        public int X;
        /// <summary>
        /// The y coordinate of the vertex.
        /// </summary>
        public int Y;
        /// <summary>
        /// The z coordinate of the vertex.
        /// </summary>
        public int Z;
    }
}
